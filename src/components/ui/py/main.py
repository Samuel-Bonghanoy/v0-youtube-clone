def generate_response(prompt, model="gpt-4", max_tokens=100, temperature=0.7):
    """
    Function to generate a response from the OpenAI API.

    Parameters:
    - prompt (str): The input prompt for the model.
    - model (str): The model to use (default is gpt-4).
    - max_tokens (int): The maximum number of tokens to include in the response.
    - temperature (float): Controls randomness. Higher values make output more random.

    Returns:
    - response (str): The model's generated response.
    """
    try:
        response = openai.Completion.create(
            engine=model,
            prompt=prompt,
            max_tokens=max_tokens,
            temperature=temperature
        )
        return response.choices[0].text.strip()
    except Exception as e:
        return f"An error occurred: {e}"

if __name__ == "__main__":
    user_prompt = input("Enter your prompt: ")
    result = generate_response(user_prompt)
    print("Response:\n", result)
